    private void cargarEstConta() {
        Preferences Pre = Preferences.userNodeForPackage(VentanaMeseroPed.class);
        contadorPedidos = Pre.getInt("contadorPedidos", 0);
    }

    private void guardarEstConta() {
        Preferences Pre = Preferences.userNodeForPackage(VentanaMeseroPed.class);
        Pre.putInt("contadorPedidos",contadorPedidos);
    }
    
     private void ReadMesasDisponibles() throws IOException {
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        BufferedReader br = new BufferedReader(new FileReader("NmesasDis.txt"));
        String line;
        while ((line = br.readLine()) != null) {
            String[] values = line.split(",");
            for (String value : values) {
                model.addElement(value);
            }
        }
        br.close();
        CmbNmesasD.setModel(model);
    }
     
    private void eliminarMesaSeleccionada() throws IOException {
    String mesaSeleccionada = CmbNmesasD.getSelectedItem().toString();

    
    File archivoOcu = new File("NmesasOcu.txt");
    BufferedReader brOcu = new BufferedReader(new FileReader(archivoOcu));
    String lineaOcu;
    boolean mesaExistente = false;
    while ((lineaOcu = brOcu.readLine()) != null) {
        if (lineaOcu.equals(mesaSeleccionada)) {
            mesaExistente = true;
            break;
        }
    }
    brOcu.close();

    if (mesaExistente) {
        JOptionPane.showMessageDialog(null,"La mesa seleccionada ya est√° ocupada");
        return;
    }

    List<String> mesasActualizadas = new ArrayList<>();

    File archivoDis = new File("NmesasDis.txt");
    BufferedReader br = new BufferedReader(new FileReader(archivoDis));
    String linea;
    while ((linea = br.readLine()) != null) {
        if (!linea.equals(mesaSeleccionada)) {
            mesasActualizadas.add(linea);
        }
    }
    br.close();

    BufferedWriter bw = new BufferedWriter(new FileWriter(archivoDis));
    for (String mesa : mesasActualizadas) {
        bw.write(mesa);
        bw.newLine();
    }
    bw.close();

    BufferedWriter bwOcu = new BufferedWriter(new FileWriter(archivoOcu, true));
    bwOcu.write(mesaSeleccionada);
    bwOcu.newLine();
    bwOcu.close();
    VerMe.actualizarComboBoxMesasOcu();

    ReadMesasDisponibles();
}

