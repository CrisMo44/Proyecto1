/**
 * Este metodo agrega a la cantidad del producto si existe en la lista de inventario y sino lo agrega como un producto nuevo
 */
    private void AddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddProductActionPerformed
        // TODO add your handling code here:
        String ProAdd = CmbProdu.getSelectedItem().toString();
        int CantidadP = Integer.parseInt(CantidadPro.getText());
        
        if(VerInv.existeProducto(ProAdd)){
        VerInv.agregarCantidadAlInventario(ProAdd, CantidadP);
        }
        else{
         VerInv.agregarNuevoProducto(ProAdd, CantidadP);
        }   
    LeerInventario();
    }//GEN-LAST:event_AddProductActionPerformed
/**
 * Este metodo lee el txt de inventario  
 */
    public void LeerInventario(){
    try (BufferedReader br = new BufferedReader(new FileReader("InventarioDis.txt"))) {
            StringBuilder contenido = new StringBuilder();
            String linea;
            while ((linea = br.readLine()) != null) {
                contenido.append(linea).append("\n");
            }
            TxtAinventario.setText(contenido.toString());
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    
     
    
    
